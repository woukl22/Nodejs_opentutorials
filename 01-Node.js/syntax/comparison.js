/* 
    이항 연산자: 왼쪽의 항과 오른쪽의 항을 계산해서 하나의 항으로 만들어준다.
    ex) console.log(1+1);   에서 '+'
    
    비교 연산자: 좌항과 우항을 비교해서 true나 false 중 하나의 값을 만들어내는 연산자이다.
*/
console.log(1==1);  // true
console.log(1==2);  // false
console.log(1>2);   // false
console.log(1<2);   // true
/* 
    참과 거짓을 표현할 때는 연산자 '==' 두 개를 써도 되는데, 왜 그래야하는지 이유를 모르면 '===' 세 개를 쓰자.
    =: 대입 연산자
    ==: 좌항과 우항이 같으냐
    ===: 좌항과 우항이 정확하게 같으냐
*/
console.log(1===1); // true
console.log(1===2); // false